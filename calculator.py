"""
命令行计算器
支持加、减、乘、除四种运算，可以连续计算。
"""

def 计算(表达式):
    """解析并计算简单的四则运算表达式，格式：数字 运算符 数字"""
    try:
        # 按空格切分：例如 "5 + 3" -> ["5", "+", "3"]
        部分 = 表达式.split()
        if len(部分) != 3:
            return "格式错误！请输入：数字 运算符 数字（例如：5 + 3）"

        数1, 运算符, 数2 = 部分[0], 部分[1], 部分[2]

        # 类型转换
        数1 = float(数1)
        数2 = float(数2)

        # 根据运算符执行计算
        if 运算符 == "+":
            结果 = 数1 + 数2
        elif 运算符 == "-":
            结果 = 数1 - 数2
        elif 运算符 == "*":
            结果 = 数1 * 数2
        elif 运算符 == "/":
            if 数2 == 0:
                return "错误：除数不能为零！"
            结果 = 数1 / 数2
        else:
            return f"不支持的运算符：{运算符}（支持 + - * /）"

        # 如果结果是整数就不显示小数点
        if 结果 == int(结果):
            结果 = int(结果)

        return f"{数1} {运算符} {数2} = {结果}"

    except ValueError:
        return "错误：请输入有效的数字！"
    except Exception as e:
        return f"计算出错：{e}"


def 主程序():
    """主循环：不断接收用户输入直到输入 quit"""
    print("=" * 40)
    print("    命令行计算器")
    print("    支持：+  -  *  /")
    print("    输入 quit 退出")
    print("=" * 40)

    while True:
        输入 = input("\n请输入算式（如 5 + 3）：").strip()
        
        if 输入.lower() == "quit":
            print("再见！👋")
            break

        if 输入 == "":
            continue

        结果 = 计算(输入)
        print(f"  {结果}")


if __name__ == "__main__":
    主程序()
